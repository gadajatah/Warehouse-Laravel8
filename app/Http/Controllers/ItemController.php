    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
         // Validated Field
       $rules = array(
        'nama' => 'required',
        'stok' => 'required',
        'harga' => 'required',
       );
       $validated = $request->validate($rules);
        // Input Data
        $item = new Item();
        $item->nama = $validated['nama'];
        $item->stok = $validated['stok'];
        $item->harga = $validated['harga'];

        if (!$item->save()) {
            return redirect()->route('create')->with('error', 'Barang gagal dibuat.');;
        }
        return redirect()->route('item.all')->with('success', 'Barang ' . $validated['nama'] . ' berhasil dibuat!');
    }

